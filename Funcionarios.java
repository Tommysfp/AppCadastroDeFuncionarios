public class Funcionarios {
    String nome;
    int idade;
    String cargo;
    double salario;
//gets e sets nome
    String getNome() {
        return nome;
    }

    void setNome(String nome) {
        this.nome = nome;
    }
//gets e sets idade
    int getIdade() {
        return idade;
    }

    void setIdade(int idade) {
        this.idade = idade;
    }
//gets e sets cargo
    String getCargo() {
        return cargo;
    }

    void setCargo(String cargo) {
        this.cargo = cargo;
    }
//gets e sets salario
    double getSalario() {
        return salario;
    }

    void setSalario(double salario) {
        this.salario = salario;
    }

}
